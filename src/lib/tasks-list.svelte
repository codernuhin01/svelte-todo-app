<script>
import { fade } from 'svelte/transition';

let {tasks, removeTask} = $props()
</script>

<section>
    {#each tasks as task}
    <article transition:fade class="task">
      <label for={task.id}><input id={task.id} type="checkbox" bind:checked={task.done}>
        <span class="{task.done ? 'done' : ''}" >{task.title}</span>
      </label>
      <button onclick={() => removeTask(task.id) } class="outline">Remove</button>
    </article>
    {/each}
</section>

<style>
  .done{
    text-decoration: line-through;
  }
  .task{
    display: flex;
    justify-content: space-between;
    align-items: center;
  }
</style>
